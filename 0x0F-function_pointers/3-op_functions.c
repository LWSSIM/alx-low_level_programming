#include "3-calc.h"

/**
 * op_add - sum
 * @a: arg
 * @b: arg
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub
 * @a: arg
 * @b: arg
 * Return: res
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul
 * @a: arg
 * @b: arg
 * Return: res
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div
 * @a: arg
 * @b: arg
 * Return: res
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder
 * @a: arg
 * @b: arg
 * Return: res
 */
int op_mod(int a, int b)
{
	return (a % b);
}
